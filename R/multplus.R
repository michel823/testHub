#' multiplier 2 nombres
#'
#' @param x Un nombre
#' @param y Un nombre
#' @return Le produit de \code{x} et \code{y}
#' @examples
#' \dontrun{
#' mult(2, 3)
#' mult(10, 2)
#' }

mult  <- function (x, y) {
  x * y }

#' sommer 2 nombres
#'
#' @param x Un nombre
#' @param y Un nombre
#' @return La somme de \code{x} et \code{y}
#' @examples
#' \dontrun{
#' plus(2, 3)
#' plus(10, 20)
#' }

plus  <- function (x, y) {
  x + y }